# -*- coding: utf-8 -*-
"""
影片畫質提升程式設定檔
可修改以下參數來調整程式行為
"""

# ============================================================
# 資料夾設定
# ============================================================
# 輸入資料夾路徑（存放原始影片）
INPUT_FOLDER = r".\input"

# 輸出資料夾路徑（存放處理後的影片）
OUTPUT_FOLDER = r".\output"

# ============================================================
# 目標解析度設定
# ============================================================
# 目標寬度（短影音標準為 1080）
TARGET_WIDTH = 1080

# 目標高度（短影音標準為 1920，9:16 比例）
TARGET_HEIGHT = 1920

# ============================================================
# 縮放設定
# ============================================================
# 縮放算法（推薦: lanczos - 最高品質但較慢, bicubic - 平衡品質與速度）
SCALE_ALGORITHM = "lanczos"

# ============================================================
# 銳化濾鏡設定 (unsharp)
# ============================================================
# 銳化強度 (0.5-1.5 之間，值越大越銳利，但可能產生噪點)
SHARPEN_AMOUNT = 1.0

# 銳化矩陣大小 (建議使用 5x5)
SHARPEN_LUMA_X = 5
SHARPEN_LUMA_Y = 5

# ============================================================
# 降噪濾鏡設定 (hqdn3d)
# ============================================================
# 是否啟用降噪 (對於已經清晰的影片可以關閉)
ENABLE_DENOISE = True

# 亮度降噪強度 (0-10，建議 2-4)
DENOISE_LUMA_STRENGTH = 3

# 色度降噪強度 (0-10，建議 2-4)
DENOISE_CHROMA_STRENGTH = 3

# ============================================================
# 輸出編碼設定
# ============================================================
# 視頻編碼器 (libx264 - 相容性最好)
VIDEO_CODEC = "libx264"

# 編碼預設 (slower - 更好的壓縮品質但較慢, medium - 平衡)
ENCODE_PRESET = "slower"

# CRF 值 (18-23 之間，值越小畫質越好但檔案越大)
# 18 = 視覺無損, 23 = 標準品質
CRF_VALUE = 18

# 輸出位元率上限 (kbps，0 = 不限制)
MAX_BITRATE = 0

# 音頻編碼器
AUDIO_CODEC = "aac"

# 音頻位元率
AUDIO_BITRATE = "192k"

# ============================================================
# 影片檔案副檔名
# ============================================================
# 被視為影片的檔案副檔名（小寫）
VIDEO_EXTENSIONS = [".mp4", ".mov", ".avi", ".mkv", ".webm", ".flv", ".wmv", ".m4v"]

# ============================================================
# 輸出檔案命名
# ============================================================
# 輸出檔案後綴（例如：video.mp4 -> video_enhanced.mp4）
OUTPUT_SUFFIX = "_enhanced"

# 輸出格式（預設保持原格式，或指定如 ".mp4"）
OUTPUT_FORMAT = None  # None = 保持原格式
